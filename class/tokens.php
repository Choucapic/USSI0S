<?php


class tokens
{
    private $uuid;

    private $name;

    private $scryfallId;

    private $scryfallIllustrationId;

    private $setcode;

    private $setlogo;

    /**
     * @return mixed
     */
    public function getUuid()
    {
        return $this->uuid;
    }

    /**
     * @return mixed
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @return mixed
     */
    public function getScryfallId()
    {
        return $this->scryfallId;
    }

    /**
     * @return mixed
     */
    public function getScryfallIllustrationId()
    {
        return $this->scryfallIllustrationId;
    }

    /**
     * @return mixed
     */
    public function getSetcode()
    {
        return $this->setcode;
    }

    /**
     * @return mixed
     */
    public function getSetlogo()
    {
        return $this->setlogo;
    }
}